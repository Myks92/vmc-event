<?php


namespace Myks92\Vmc\Event\Model\Entity\Events;


/**
 * Class Id
 * @author Maxim Vorozhtsov <myks1992@mail.ru>
 */
class Id
{
    /**
     * @var string
     */
    private $value;

    /**
     * Id constructor.
     * @param string $value
     */
    public function __construct(string $value)
    {
        $this->value = $value;
    }

    /**
     * @return string
     */
    public function getValue(): string
    {
        return $this->value;
    }

    /**
     * @return string
     */
    public function __toString(): string
    {
        return $this->value;
    }
}